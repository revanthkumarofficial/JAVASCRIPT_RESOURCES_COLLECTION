/** 
JavaScript has ONE Number type
Positive numbers!
Negative numbers!
Whole numbers!
Decimal numbers!
 */



/** POSITIVE NUMBERS */
var num1 = 20;
console.log(num1);   // 20


/** NEGATIVE NUMBERS */
var num2 = -40;
console.log(num2);   // -40


/** WHOLE NUMBERS (NO DECIMAL PART AND NO NEGATIVE NUMBERS) */
var wnum1 = 10;
var wnum2 = 0;
console.log(wnum1);   // 10
console.log(wnum2);   // 0

/** DECIMAL NUMBERS */
var dnum1 = 90.6;
var dnum2 = -30.5;


console.log(dnum1);   // 90.6
console.log(dnum2);   // -30.5